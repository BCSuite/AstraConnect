import Foundation


/// A namespace to palce universal informations about a Solana request.
public enum ACSolanaRequestSpace {
    
    /// The data type name of Solana's RPC.
    public static let ACSolanaRPCTypeName = "jsonrpc"
    
    /// The version of  Solana's RPC.
    public static let ACSolanaRPCTypeVersion = "2.0"
    
    /// The  'id'  description of  Solana's RPC.
    public static let ACSolanaRPCIDDesc = "id"

    /// The  detailed value of 'id' in Solana's RPC.
    public static let ACSolanaRPCIDValue = 1

}

